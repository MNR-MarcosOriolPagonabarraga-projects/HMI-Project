function EEG = build_eeglab_dataset(data, fs, chanlocs, set_name)
    EEG = pop_importdata( ...
        'dataformat', 'array', ...
        'nbchan', size(data, 1), ...
        'data', data, ...
        'srate', fs);

    EEG.setname = set_name;
    EEG.chanlocs = chanlocs;
    EEG = eeg_checkset(EEG);
end
